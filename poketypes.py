def reversable(cycle):
    if len(cycle) < 2:
        return False
    for i in range(len(cycle)-1,-1,-1):
        attacker = cycle[i]
        victim = cycle[i-1]
        if type_chart[attacker][victim] > 0.5:
            return False
    return True